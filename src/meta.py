class Meta:
    """
    Clase que representa a la meta.
    """

    def __init__(self, x, y):
        """
        Inicializa una instancia de la clase.
        :param x: Posición en el eje x en el que se encuentra la meta.
        :param y: Posición en el eje y en el que se encuentra la meta.
        """
        self.x = x
        self.y = y